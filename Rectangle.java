public class Rectangle
{
    public static void main(String args[])
    {
        int m = 2;
        int n = 4;
        for (int i= 0;i<m;i++)
        {
            for (int j = 0; j<n;j++)
            {
                System.out.print(8);
            }
            System.out.println();
        }
    }
}
